#define OLED_CS_Set()  GPIO_SetBits(GPIOB,OLED_CS)

#define OLED_DC_Clr()  GPIO_ResetBits(GPIOC,OLED_DC)
#define OLED_DC_Set()  GPIO_SetBits(GPIOC,OLED_DC)
 					   
#define OLED_RST_Clr()  GPIO_ResetBits(GPIOC,OLED_RST)
